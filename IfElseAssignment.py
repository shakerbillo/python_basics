"""
- Create a variable grade holding an integer between 0 - 100

- Code if, elif, else statements to print the letter grade of the number grade variable

Grades:

A = 90 - 100

B = 80 - 89

C = 70-79

D = 60 - 69

E = 50 - 59

F = 0 - 49



"""

grade = 100

if grade >= 90:
    print("A")

elif grade >= 80 and grade < 90:
    print("B")

elif grade >= 70 < 80:
    print("C")

elif grade >= 60 < 70:
    print("D")

elif grade >= 50 < 60:
    print("E")

else:
    print("F")
